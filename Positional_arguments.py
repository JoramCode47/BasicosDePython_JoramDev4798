# POSITIONAL ARGUMENTS

def imprimir_mensaje(primer_nombre, segundo_nombre, primer_apellido, segundo_apellido):
    print(f"""Hola {primer_nombre} 
    {segundo_nombre} 
    {primer_apellido}
    {segundo_apellido}""")

imprimir_mensaje("Eduardo", "Joel", "Ramírez", "Martínez")

