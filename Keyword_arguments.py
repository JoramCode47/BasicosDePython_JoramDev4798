# KEYWORD ARGUMENTS

def imprimir_mensaje(primer_nombre, segundo_nombre, primer_apellido, segundo_apellido):
    print(f"""Hola {primer_nombre} 
    {segundo_nombre} 
    {primer_apellido}
    {segundo_apellido}""")

imprimir_mensaje(primer_nombre="Eduardo", segundo_nombre="Joel", segundo_apellido="Martínez", primer_apellido="Ramírez")

